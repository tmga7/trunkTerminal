# Site Status
STATUS_SITE_OFFLINE = 0x10  # 0001 0000 -> 16
STATUS_SITE_SITE_TRUNKING = 0x20  # 0010 0000 -> 32
STATUS_SITE_FAILSOFT = 0x40  # 0100 0000 -> 64
STATUS_SITE_IMPAIRED = 0x80  # 1000 0000 -> 128
STATUS_SITE_WIDEAREA = 0x100  # 0001 0000 0000 -> 256

# Unit Registration
REG_ACCEPT = 0x00  # indicates that registration is accepted.
REG_FAIL = 0x01  # indicates that the RFSS was unable to verify registration.
REG_DENY = 0x10  # indicates that registration is not allowed at the location.
REG_REFUSED = 0x10  # indicates that the WUID is invalid but the SU need not enter trunking hunt and the SU may attempt to re-register after a user stimulus.

# Radio Types
RADIO_TYPE_SUBSCRIBER = 0x01  # 0000 0001 -> 1
RADIO_TYPE_CONSOLE = 0x02  # 0000 0010 -> 2
RADIO_TYPE_SYSTEM = 0xFFFFFF  # 0000 0011 -> 3

